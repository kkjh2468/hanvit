package java0917;

public class Gesipan {

	private int b;
	private String j;
	private String ho;
	private String n;

	public int getB() {
		return b;
	}
	public void setB(int b) {
		this.b = b;
	}
	public String getJ() {
		return j;
	}
	public void setJ(String j) {
		this.j = j;
	}
	public String getHo() {
		return ho;
	}
	public void setHo(String ho) {
		this.ho = ho;
	}
	public String getN() {
		return n;
	}
	public void setN(String n) {
		this.n = n;
	}
	@Override
	public String toString() {
		return "Gesipan [b=" + b + ", j=" + j + ", ho=" + ho + ", n=" + n + "]";
	}
	
}
